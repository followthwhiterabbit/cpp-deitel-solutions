#include <iostream>
#include <cmath>
using namespace std;

int main()
	{
		
		
		// the value of x rounded downward(as a floating-point value)
		
		cout << "floor of 2.3 is " << floor(2.3) << endl;  
		cout << "floor of 3.8 is " << floor(3.8) << endl; 
		cout << "floor of -2.3 is " << floor(-2.3) << endl; 
		cout << "floor of -3.8 is " << floor(-3.8) << endl;
		
		
	}
