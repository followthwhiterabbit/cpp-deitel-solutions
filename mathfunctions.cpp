#include <iostream>
#include <cmath>

using namespace std;


int main()
	{
		double c = 9.2; 
		double x = 0.0; 
		
		cout << ceil(c) << endl;   // rounds x to the smallest integer not less than x
		cout << static_cast<double>(cos(x))<< endl;
		cout << exp(1.0) << endl;
		cout << exp(2.0) << endl;
		cout << fabs(-5.1) << endl; 
		cout << floor(9.2) << endl; // rounds x to the largest integer not greater than x
		cout << fmod(5, 2) << endl; // remainder of x/y as a floating-point number
		cout << sin(0.0) << endl;
		cout << sqrt(9.0) << endl; // square root of x (where x is a nonnegative value)
		cout << tan(0.0) << endl;
		
		
	}
